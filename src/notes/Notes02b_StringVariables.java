package notes;

/*
 * Notes over String Variables
 * 
 * variables are like shoe boxes, they store and represent single bits of data
 * Strings = text
 * String variable = it's the shoe box
 * String literal = literally the stuff between the quotes ""
 */

public class Notes02b_StringVariables {

	public static void main(String[] args) {
		
		//declare variable - creates the box
		String name;
		
		//initialize - gives the box it's first value
		name = "Robert";
		//now name is ready to be used.
		
		System.out.println(name);
		
		//we can declare and initialize in 1 line
		String address = "1313 Pennsylvania Ave.";
		
		System.out.println(address + " " + name);
		
		
		
		
		
	}
	
	
	
}
